#!/bin/bash
# Скрипт копіює всі файли певного формату в /tmp/newdir

# Перевірка наявності параметру
if [ $# -ne 1 ]; then
    echo "Використання: $0 <формат файлів, наприклад *.txt>"
    exit 1
fi

FORMAT=$1           # Приймаємо формат файлів як перший параметр
DEST_DIR="/tmp/newdir"

# Створюємо папку призначення, якщо її немає
mkdir -p "$DEST_DIR"

# Копіюємо файли
cp $FORMAT "$DEST_DIR"

echo "Файли $FORMAT скопійовано в $DEST_DIR"